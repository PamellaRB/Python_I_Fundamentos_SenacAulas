#Espaço de memoria sem um tempo definido
#Guardar uma informação

nome = "Pamella"
print(nome)
# nome é a variavel;
# Sinal = é atribuição;
# Python trabalha da direita para esquerda
# Ou seja esta atribuindo o nome Pamella na variavel nome
# "Print" irá executar no terminal a variável. 
# Em Python já puxa automaticamente o tipo da variavel, não precisa declarar na variavel se é texto ou número

#--------------------------------------------------------------------------------------------------------------

# Os tipos armazenam variaveis diferentes
# INT = Numeros inteiros (1,2,3,4...)

idade = 31
numero_de_alunos = 15

# Ponto Flutuante (flut)
# Armazena números que possuem casas decimais (10.3, 20.4...)
# Sempre utiliza ponto "."

salario = 30.10
altura = 1.75
peso = 40.30

# Sinal de "+" em conjunto com o texto realiza uma junção de informações
# STR converte numeros em texto, sendo assim, é possível juntar a informação de texto com números.

print("Minha idade: " + str(idade) + "Numero de alunos: " + str(numero_de_alunos))

# STR - String - Armazena uma sequência de catacteres (palavras)
nome = "Pamella"
sobrenome = "Barros"

print(nome + sobrenome)

